package Demo;

public class fraction {
	private int T;
	private int M;

	public fraction() {
		setT(1);
		setM(2);
	}

	public int getT() {
		return T;
	}

	public void setT(int t) {
		T = t;
	}

	public int getM() {
		return M;
	}

	public void setM(int m) {
		M = m;
	}
}
